    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        int selectedRow = jTableEmployee.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this,
                "Select a row to delete",
                "No row selected", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                int employee_id =
                Integer.parseInt(jTableEmployee.
                    getValueAt(selectedRow, 0).toString());
                Statement stmt = con.createStatement();
                stmt.execute("Delete From employees Where employee_id = " + employee_id);
                refreshData();
                jTextFieldEmployeeId.setText(null);
                jTextFieldEmployeeName.setText(null);
                jTextFieldEmployeeLastName.setText(null);
                jTextFieldEmployeePhone.setText(null);
               
                jTextFieldEmployeeDob.setText(null);
                jTextFieldEmployeeAddress.setText(null);
                
                jTextFieldEmployeePassword.setText(null);
            }catch(SQLException ex){
                System.err.println(ex.getMessage());
            }

        }

    }//GEN-LAST:event_jButtonDeleteActionPerformed

