            + "employee_gender='"+employee_gender+"', employee_dob ='"+employee_dob+"',"
            + "employee_phone ='"+employee_phone+"', employee_address ='"+employee_address+"',"
            + "employee_position ='"+employee_position+"',employee_password='"+employee_password+"' where employee_id ='"+employee_id+"'";
